package com.example.examenfranciscointerfaces;

public class Persona {
    private String nombre;
    private int edad;
    private String sexo;
    private double peso;
    private Integer altura;
    private String actividad;
    private int ger;
    private int get;
    public Persona(String nombre, int edad, String sexo, double peso, Integer altura, String actividad, int ger, int get) {
        this.nombre = nombre;
        this.edad = edad;
        this.sexo = sexo;
        this.peso = peso;
        this.altura = altura;
        this.actividad = actividad;
        this.ger = ger;
        this.get = get;
    }


}
